


let valor= parseInt(prompt("ingrese un numero del 1 al 7"))

switch (valor){
    case 1:
        document.write("uno");
        break;


    case 2:
        document.write("dos");
        break;

    case 3:
        document.write("tres");
        break;


    case 4:
        document.write("cuatro");
        break;

    case 5:
        document.write("cinco");
        break;
    case 6:
        document.write("seis");
        break;

    case 7:
        document.write("siete");
        break;

    default:
        document.write("numero no valido");
        break;


}